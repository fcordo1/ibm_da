	ON "FCORDO"."FACT_VENTAS" ("ID_COMPRADOR");

ALTER TABLE "FCORDO"."DIM_COMPRADORES" ADD CONSTRAINT "DIM_COMPRADORES_PK" PRIMARY KEY
	("ID_COMPRADOR") USING INDEX "FCORDO"."DIM_COMPRADORES_PK_IDX";

ALTER TABLE "FCORDO"."FACT_VENTAS" ADD CONSTRAINT "FACT_VENTAS_PK" PRIMARY KEY
	("ID_COMPRADOR") USING INDEX "FCORDO"."FACT_VENTAS_PK_IDX";

ALTER TABLE "FCORDO"."FACT_VENTAS" ADD CONSTRAINT "FACT_VENTAS_DIM_COMPRADORES_FK" FOREIGN KEY
	("ID_COMPRADOR")
	REFERENCES "FCORDO"."DIM_COMPRADORES"
	("ID_COMPRADOR");

